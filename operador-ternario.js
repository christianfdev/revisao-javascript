/**
 * Apenas uma revisão de uma situação especifica de Operador Ternário
 * 
 */


let idade = 20;


// Utilizando a vírgula para passar mais instruções nos retornos de um operador ternário
idade > 18 ? (
    console.log('Parabéns! Você está na idade para isso!'),
    console.log('Pode continuar sua jornada conosco!')
) : (
    console.log('Você foi reprovado!'),
    console.log('Suma daqui!')
);


// OBS.: Aninhamento de operadores ternários é considerada uma má prática, então evite!